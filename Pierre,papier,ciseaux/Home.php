        <strong>REGLE DE JEU.</strong>Pierre-feuille-ciseaux est un jeu qui se joue avec les mains, et à deux joueurs. Chacun des joueurs met une main dans le dos et va choisir un des coups possibles (pierre/feuille ou ciseaux), et le symboliser avec sa main, toujours dans son dos. Pour le coup "pierre", le joueur ferme le poing. Pour le coup "feuille", il met sa main à plat. Et pour le dernier coup possible, le "ciseau", le joueur écarte ses deux doigts. Les deux joueurs révèlent ensuite leur main, et donc, le coup qu’ils décident de jouer, simultanément. De façon générale : - Le jeux de "pierre" bat les ciseaux (en les écrasant).
        - Le jeux de "ciseaux" bat la feuille (en la coupant).
        - La "feuille" bat la pierre (en l'enveloppant). 
        Ainsi, chaque coup bat un autre coup. 
       Si les joueurs révèlent le même symbole (pierre/pierre, feuille/feuille, ciseaux/ciseaux), cela fait match nul : personne ne marque de points..
